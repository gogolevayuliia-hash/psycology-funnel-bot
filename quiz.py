def calculate_result(answers: list[tuple[str, int]]) -> str:
    """
    answers — список (тип, балл) для каждого вопроса.
    Возвращает тип привязанности.
    """
    scores = {"anxious": 0, "avoidant": 0, "fearful": 0}
    for atype, score in answers:
        if atype in scores:
            scores[atype] += score

    best_type = max(scores, key=lambda k: scores[k])
    best_score = scores[best_type]

    if best_score < 3:
        return "Надёжный"

    return {
        "anxious": "Тревожный",
        "avoidant": "Избегающий",
        "fearful": "Тревожно-избегающий",
    }[best_type]
