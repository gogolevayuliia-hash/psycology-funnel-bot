def calculate_result(answers: list[tuple[int, str | None]]) -> str:
    """
    answers — список (total_score, pattern_or_None) длиной 12.
    Возвращает код результата: 'П1' или 'П2-Т' / 'П3-О' / 'П4-Смешанный' и т.п.
    """
    total = sum(s for s, _ in answers)
    counts: dict[str, int] = {"Т": 0, "О": 0, "Н": 0}
    for s, p in answers:
        if p:
            counts[p] += s

    if total <= 7:
        return "П1"
    if total <= 14:
        level = "П2"
    elif total <= 21:
        level = "П3"
    else:
        level = "П4"

    ranked = sorted(counts.items(), key=lambda kv: kv[1], reverse=True)
    top_pattern, top_score = ranked[0]
    second_score = ranked[1][1] if len(ranked) > 1 else 0
    # Если паттерны не выражены — фоллбек в Смешанный.
    if top_score == 0 or (top_score - second_score) <= 2:
        return f"{level}-Смешанный"
    return f"{level}-{top_pattern}"
