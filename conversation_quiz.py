def calculate_result(answers: list[tuple[str, int]]) -> str:
    scores = {"criticism": 0, "contempt": 0, "defense": 0, "wall": 0, "secure": 0}
    for atype, score in answers:
        if atype in scores:
            scores[atype] += score

    max_score = max(scores.values())
    # При равенстве — приоритет secure, потом остальные по порядку
    priority = ["secure", "criticism", "contempt", "defense", "wall"]
    for ptype in priority:
        if scores[ptype] == max_score:
            return ptype
